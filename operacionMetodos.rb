#operacion de suma con metodos

def suma(num1,num2)
    resultado = num1 + num2
    puts resultado
end

suma(5,5)

#operacion de resta
def resta(num1,num2)
    resultado = num1 - num2
    puts resultado
end

resta(7,4)

#operacion multiplicacion
def multiplicacion(num1,num2)
    resultado = num1 * num2
    puts resultado
end

multiplicacion(8,8)

#operacion division
def division(num1,num2)
    resultado = num1/num2
    puts resultado
end

division(4,4)

#operacion modulo(devuelve el sobrante)

def aritme(num1,num2)
    resultado = num1 % num2
    puts resultado
end

aritme(100,30)
    